package com.example.sweet.data.model;

public class Category {

    private Long id;
    private String name;
    private String imageUrl;
    private String description;

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public String getDescription() {
        return description;
    }
}
